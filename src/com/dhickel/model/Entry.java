package com.dhickel.model;

/**
 * The type Entry.
 */
public abstract class Entry {
    /**
     * The id of derived type.
     */
    int id;
    /**
     * The Name.
     */
    String name;

    /**
     * Gets id from derived type.
     *
     * @return the id
     */
    public int getID() {
        return id;
    }

    /**
     * Gets name.
     *
     * @return the name
     */
    public String getName() {
        return name;
    }


}
